  }

  set setData1(String value) {
    data1 = value;
  }

//
  String get getData2 {
    return data2;
  }

  set setData2(String value) {
    data2 = value;
  }

//
  String get getData3 {
    return data3;
  }

  set setData3(String value) {
    data3 = value;
  }
}
